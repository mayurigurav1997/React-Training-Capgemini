let remove_accounts =[]
let updated_accounts =[]
for(let account of bank_app){
    let flag= false
    for(let trans of account.transaction){
       if(trans.type=="WITHDRAW"){
          flag= true;
          break;
       }
    }
    if(flag){
        updated_accounts.push(account)
    }else{
        remove_accounts.push(account)
    }
}
